import { execFileSync } from 'node:child_process';
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, '../..');
const openAPIPath = join(repoRoot, 'hack/view2ts/dist/view.openapi.json');
const outPath = join(repoRoot, 'ui/shared/src/types/generated/view.gen.ts');

const header = `// Code generated by \`make generate-ui-types\`. DO NOT EDIT.

`;

mkdirSync(dirname(outPath), { recursive: true });

const generated = execFileSync(
  'npx',
  [
    'openapi-typescript',
    openAPIPath,
    '--export-type',
    '--default-non-nullable',
    'false',
  ],
  { encoding: 'utf8', cwd: __dirname },
);

writeFileSync(outPath, header + generated);

const expectedRoots = ['PromotionStrategyDetails', 'PromotionStrategyDetailsList', 'PromotionStrategy'];
const openAPI = JSON.parse(readFileSync(openAPIPath, 'utf8'));
const schemaKeys = Object.keys(openAPI.components?.schemas ?? {});
const missing = expectedRoots.filter((k) => !schemaKeys.includes(k));
if (missing.length > 0) {
  console.error(`missing root schemas in OpenAPI bundle: ${missing.join(', ')}`);
  process.exit(1);
}

console.log(`wrote ${outPath} (${schemaKeys.length} schemas)`);

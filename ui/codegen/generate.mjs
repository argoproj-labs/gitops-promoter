import { execFileSync } from 'node:child_process';
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, '../..');
const openAPIPath = join(repoRoot, 'hack/crd2ts/dist/crds.openapi.json');
const outPath = join(repoRoot, 'ui/shared/src/types/generated/crds.gen.ts');

const header = `// Code generated by \`make generate-ui-types\`. DO NOT EDIT.

`;

mkdirSync(dirname(outPath), { recursive: true });

const generated = execFileSync(
  'npx',
  [
    'openapi-typescript',
    openAPIPath,
    '--export-type',
    '--default-non-nullable',
    'false',
  ],
  { encoding: 'utf8', cwd: __dirname },
);

writeFileSync(outPath, header + generated);

// Sanity check: all 13 promoter kinds are present.
const expectedKinds = [
  'PromotionStrategy',
  'PullRequest',
  'ChangeTransferPolicy',
  'GitRepository',
  'CommitStatus',
  'GitCommitStatus',
  'ArgoCDCommitStatus',
  'TimedCommitStatus',
  'WebRequestCommitStatus',
  'ScmProvider',
  'ClusterScmProvider',
  'ControllerConfiguration',
  'RevertCommit',
];
const openAPI = JSON.parse(readFileSync(openAPIPath, 'utf8'));
const schemaKeys = Object.keys(openAPI.components?.schemas ?? {});
const missing = expectedKinds.filter((k) => !schemaKeys.includes(k));
if (missing.length > 0) {
  console.error(`missing schemas in OpenAPI bundle: ${missing.join(', ')}`);
  process.exit(1);
}

console.log(`wrote ${outPath}`);
